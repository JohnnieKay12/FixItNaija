const serviceInput = document.getElementById('serviceInput');
const locationInput = document.getElementById('locationInput');
const searchBtn = document.getElementById('searchBtn');
const searchResults = document.getElementById('searchResults');

function renderResults(results) {
searchResults.innerHTML = ''; // Clear previous results

if (results.length === 0) {
searchResults.innerHTML = `<p class="text-center text-gray-600 col-span-full">No results found</p>`;
return;
}

results.forEach(artisan => {
const card = document.createElement('div');
card.className = "flex items-center gap-4 p-4 border rounded-xl bg-white text-black hover:bg-[#d97706] hover:text-white transition cursor-pointer shadow";

card.innerHTML = `
<img src="${artisan.img}" alt="${artisan.name}'s image" class="w-16 h-16 rounded-full object-cover" />
<div>
<h3 class="text-lg font-semibold">${artisan.name}</h3>
<p class="text-sm">${artisan.service} - ${artisan.location}</p>
</div>
`;

card.onclick = () => {
openModal(artisan);
searchResults.innerHTML = '';
serviceInput.value = '';
locationInput.value = '';
};

searchResults.appendChild(card);
});
}

function openModal(artisan) {
document.getElementById('profileModalImg').src = artisan.img;
document.getElementById('profileModalName').textContent = artisan.name;
document.getElementById('profileModalService').textContent = artisan.service;
document.getElementById('profileModalLocation').textContent = artisan.location;
document.getElementById('profileModalDesc').textContent = artisan.desc;
document.getElementById('profileModalRating').textContent = `â­ ${artisan.rating}`;
document.getElementById('profileModalWhatsapp').href = `https://wa.me/${artisan.phone}?text=Hi%20${encodeURIComponent(artisan.name)},%20I'm%20interested%20in%20your%20${encodeURIComponent(artisan.service)}%20services.`;

const modal = document.getElementById('profileModal');
modal.classList.remove('hidden');
modal.classList.add('flex');
}

function closeModal() {
const modal = document.getElementById('profileModal');
modal.classList.add('hidden');
modal.classList.remove('flex');
}

searchBtn.addEventListener('click', () => {
const serviceQuery = serviceInput.value.trim().toLowerCase();
const locationQuery = locationInput.value.trim().toLowerCase();

if (!serviceQuery && !locationQuery) {
alert('Please enter a service or location to search.');
return;
}

const filtered = artisans.filter(a => {
const serviceMatch = a.service.toLowerCase().includes(serviceQuery) || a.name.toLowerCase().includes(serviceQuery);
const locationMatch = locationQuery ? a.location.toLowerCase().includes(locationQuery) : true;
return serviceMatch && locationMatch;
});

renderResults(filtered);
});




